package view;

import javafx.scene.control.Label;

/**
 * A simple status label that prepends the word "Status: " to the status
 */
public class StatusLabel extends Label {
    
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    // Constructors
    ////////////////////////////////////////////////////////////////////////////

    /**
     * Construct a StatusLabel object
     */
    public StatusLabel() {
        super("Status: -");
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    // Setter Methods
    ////////////////////////////////////////////////////////////////////////////

    /**
     * Set the status to the given text
     *
     * @param text The status value to set
     */
    public void setStatus(String text) {
        this.setText("Status: " + text);
    }

}
